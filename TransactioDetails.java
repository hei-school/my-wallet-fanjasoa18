public class TransactionDetails {
    private String date;
    private String type;
    private String compte;
    private String montant;
    private String argent;
    private String carteBancaire;
    private String permis;
    private String photosIdentite;
    private String cartesVisite;
    private String categorie;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getCompte() {
        return compte;
    }

    public void setCompte(String compte) {
        this.compte = compte;
    }

    public String getMontant() {
        return montant;
    }

    public void setMontant(String montant) {
        this.montant = montant;
    }

    public String getArgent() {
        return argent;
    }

    public void setArgent(String argent) {
        this.argent = argent;
    }

    public String getCarteBancaire() {
        return carteBancaire;
    }

    public void setCarteBancaire(String carteBancaire) {
        this.carteBancaire = carteBancaire;
    }

    public String getPermis() {
        return permis;
    }

    public void setPermis(String permis) {
        this.permis = permis;
    }

    public String getPhotosIdentite() {
        return photosIdentite;
    }

    public void setPhotosIdentite(String photosIdentite) {
        this.photosIdentite = photosIdentite;
    }

    public String getCartesVisite() {
        return cartesVisite;
    }

    public void setCartesVisite(String cartesVisite) {
        this.cartesVisite = cartesVisite;
    }

    public void setCategorie(String categorie) {
        this.categorie= categorie;
    }

    public String getCategorie() {
        return categorie;
    }
}
